    private void addProductButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addProductButtonActionPerformed
        if (AddMaSP.getText().equals("")
            || AddTenSP.getText().equals("")
            || addGia.getText().equals("")
            || addKhuyenMai.getText().equals("")
            || addSoLuong.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "You must enter all the fields!",
                "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        List<ProductData> PrDatas = handle.GetAllProductData();
        for (ProductData PrData : PrDatas) {
            if (Integer.valueOf(AddMaSP.getText()).equals(PrData.getmaSP())) {
                JOptionPane.showMessageDialog(rootPane, "ID already exists!",
                    "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        ProductData PrData = new ProductData();
        PrData.setmaSP(Integer.valueOf(AddMaSP.getText()));
        PrData.setName(AddTenSP.getText());
        PrData.setgia(Integer.valueOf(addGia.getText()));
        PrData.setkhuyenMai(addKhuyenMai.getText());
        PrData.setsoLuong(Integer.valueOf(addSoLuong.getText()));
        handle.AddNewProductData(PrData);
        JOptionPane.showMessageDialog(rootPane, "Success!");
        AddMaSP.setText("");
        AddTenSP.setText("");
        addGia.setText("");
        addKhuyenMai.setText("");
        addSoLuong.setText("");
        tableModel.setRowCount(0);
        SetModelTable(handle.GetAllProductData());
    }//GEN-LAST:event_addProductButtonActionPerformed

