    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        String valueCombobox = searchByCombobox.getSelectedItem().toString();

        List<ProductData> resultSearch = handle.GetResultSearch(valueCombobox, searchBarTF.getText());
        resultSearchTableModel.setRowCount(0);

        if (!resultSearch.isEmpty()) {
            SetResultSearchModelTable(resultSearch);
        } else {
            notiSearchLabel.setText(valueCombobox + " Doesn't exist!");
        }
    }//GEN-LAST:event_searchButtonActionPerformed

